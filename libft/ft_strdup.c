#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*str;
	int		i;
	size_t	slen;

	i = 0;
	slen = ft_strlen(s);
	str = malloc(slen + 1);
	if (!str)
		return (NULL);
	while (s[i])
	{
		str[i] = s[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
